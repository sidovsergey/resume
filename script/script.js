// Задание 3.Написать функцию, которая фильтрует массив оставляя только парные числа: getEven(arr).
// Перебираем входящий массив с помощью цикла, используя длину массива.
// Ищем парный элемент и оставляем его, перебираем массив.
// С помощью метода push создаем новый массив из парных чиссел.
// Используем массивы которыми пользовались выше.

function getEven(arr) {
    let aut = [];
    for (let i = 0; i < arr.length; i++) {
        if (arr[i] % 2 == 0) {
            aut.push(arr[i]);
        }
    }
    return aut;
}

