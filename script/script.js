// Задание 4.Написать функцию searchByName, которая принимант параметром строку и возвращает массив со всеми обьектами, в имени которых эта строка содержится:  searchByName(name)
// Создаем масси с вложенными в нее обьктами.(Массив дали в тестовом задании).
// С помощбю цикла перебираем в массиве все элементы(обьекты).
// Получаем доступ к ключам в обьектах и  с помощью ключей проверяем все свойства обьекта, для сравнения их с параметром, который принимаем.
// С помощью метода push создаем новый массив из обьектов, в имени которых эта строка содержится.

let someobject = [
    {
        name: 'Jonny Walker',
        birthDate: '1995-12-17'
    },
    {
        name: 'Andrew',
        birthDate: '2001-10-29'
    },
    {
        name: 'Viktor',
        birthDate: '1998-11-09'
    }]


function searchByName(name) {
    let newArr = [];
    for (let i = 0; i < someobject.length; i++) {
        let c = someobject[i];
        for (let key in c) {
            if (name == c[key]) {
                newArr.push(c);
            }
        }
    }
    return newArr;
}
