// Задание 2. Написать функцию, которая возвращает минимальный элемент массива: GetMin(arr)
// Создаем массив.
// Создаем функцию, которая будет возвращать минимальный элемент.
// Перебираем входящий массив с помощью цикла, используя длину массива.
// Ищем минимальный элемент методом сравнения двух соседних элементов.
// Возвращаем элемент минимальний с помощью return.

let arr = [2, 4, 5, 7, 9, 4, 1, 16];

function GetMin(arr) {
    let min = arr[0];
    for (let i = 0; i < arr.length; i++) {
        if (arr[i] < min) {
            min = arr[i];
        }
    }
    return min;
}
